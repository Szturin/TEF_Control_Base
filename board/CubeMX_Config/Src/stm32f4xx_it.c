/**
  * @brief This function handles USART6 global interrupt.
  */
void USART6_IRQHandler(void)
{
  /* USER CODE BEGIN USART6_IRQn 0 */
    if((__HAL_UART_GET_FLAG(&huart6,UART_FLAG_IDLE) != RESET))
    {
        __HAL_UART_CLEAR_IDLEFLAG(&huart6);
        HAL_UART_DMAStop(&huart6);
        RxLength  =  RxBuff_SIZE - __HAL_DMA_GET_COUNTER(&hdma_usart6_rx);
        RxEndFlag  = 1;
        memcpy(Judge_Buffer,RxBuff,RxLength);

        Judgment_data_Get(Judge_Buffer);

        RxLength = 0;
        RxEndFlag = 0;

        memset(RxBuff,0,RxLength);
        memset(Judge_Buffer, 0, JUDGE_BUFFER_LEN);

        HAL_UART_Receive_DMA(&huart6, RxBuff, RxBuff_SIZE);
    }
  /* USER CODE END USART6_IRQn 0 */
  HAL_UART_IRQHandler(&huart6);
  /* USER CODE BEGIN USART6_IRQn 1 */
  /* USER CODE END USART6_IRQn 1 */
}

